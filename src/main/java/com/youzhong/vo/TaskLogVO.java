package com.youzhong.vo;

public class TaskLogVO {

	private int gameRoleId;
	private int accountId;
	private int weixinId;
	private int taskTypeId;
	private double min;
	private double max;
	public int getGameRoleId() {
		return gameRoleId;
	}
	public void setGameRoleId(int gameRoleId) {
		this.gameRoleId = gameRoleId;
	}
	public int getAccountId() {
		return accountId;
	}
	public void setAccountId(int accountId) {
		this.accountId = accountId;
	}
	public int getWeixinId() {
		return weixinId;
	}
	public void setWeixinId(int weixinId) {
		this.weixinId = weixinId;
	}
	public int getTaskTypeId() {
		return taskTypeId;
	}
	public void setTaskTypeId(int taskTypeId) {
		this.taskTypeId = taskTypeId;
	}
	public double getMin() {
		return min;
	}
	public void setMin(double min) {
		this.min = min;
	}
	public double getMax() {
		return max;
	}
	public void setMax(double max) {
		this.max = max;
	}
	
	
}
